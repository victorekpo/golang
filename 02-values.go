package main

import (
  "fmt"
  )

func main() {
    fmt.Println("go" + "lang") // Strings can be added together with +.

    fmt.Println("1+1 =", 1+1)  // Integers and floaots
    fmt.Println("7.0/3.0 =", 7.0/3.0) 

    fmt.Println(true && false) // booleans
    fmt.Println(true || false)
    fmt.Println(!true)
}

